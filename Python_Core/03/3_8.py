"""Іноді виникає необхідність, щоб деякі ключові параметри були доступні лише за ключем, а не як позиційні аргументи. Для цього їх треба оголосити після параметра з зірочкою. Наприклад

def modeling(factor, *numbers, correction):
    result = 0
    for number in numbers:
        result += number * factor
    result = result - correction
    return result


print(modeling(10, 1, 2, 3, correction=2))  # 58
Оголошення параметра correction після параметра зі зірочкою дає лише ключові аргументи. Якщо ми для аргументу не вкажемо значення за замовчуванням або не передамо його під час виклику, звернення до функції викликає помилку.

TypeError: modeling() missing 1 required keyword-only argument: 'correction'
Якщо нам потрібні аргументи, що передаються лише по ключу, але не потрібен параметр з зірочкою, то при оголошенні функції можна вказати просто зірочку з підкресленням, без вказування імені:

def modeling(factor, *_, correction):"""


"""Онлайн-магазин "У Бобра" надає послугу експрес доставлення своїх товарів за ціною 5¤ за перший товар у замовленні та 2¤ - за всі наступні товари. Необхідно реалізувати функцію, яка приймає як перший параметр кількість товарів у замовленні quantity, але також має бути присутнім параметр, що передається тільки за ключем discount.

Параметр discount за замовчуванням має значення 0 - знижки немає. Приймає значення від 0 до 1. Функція cost_delivery повертає загальну суму за доставлення товару з урахуванням знижки.

Треба передбачити, що функція cost_delivery при визові може приймати будь-яку кількість позиційних аргументів.

Приклад:

cost_delivery(2, 1, 2, 3)
При такому виклику quantity дорівнює 2, а параметр discount за умовчанням має значення 0.

Тестові виклики функції для правильності роботи будуть наступними:

cost_delivery(2, 1, 2, 3) == 7
cost_delivery(3, 3) == 9
cost_delivery(1) == 5
cost_delivery(2, 1, 2, 3, discount=0.5) == 3.5"""


def cost_delivery(quantity, *args, discount=0.0, **kwargs):
    price = 5 + (2 * (quantity - 1))
    price = price - price * discount
    return price

result = cost_delivery(2, 1, 2, 3, discount=0.5)
print(result)